export const getAthletesNeedingAttention = () => 
  mockAthletes.filter(a => a.injuryRisk === "High" || a.checkInStatus === "missed" || a.compliance < 60);

export const getExpiringAthletes = () => {
  const now = new Date();
  return mockAthletes.filter(a => {
    const expiry = new Date(a.subscriptionExpiry);
    const diffDays = Math.ceil((expiry.getTime() - now.getTime()) / (1000 * 60 * 60 * 24));
    return diffDays <= 3 && diffDays > 0;
  });
};